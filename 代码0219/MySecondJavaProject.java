class MySecondJavaProject
{
	public static void main(String[] args) 
	{
		// 字面量：

		// 整型：
		// byte, short, int, long
		byte a = -128;
		short b = 20;
		int c = 30;
		long d = 40;

		// 浮点型：
		// 小数字面量，默认是double类型的
		// float, double
		// float: 需要以f作为结尾
		float e = 3.14f;
		double f = 3.14d;

		// 布尔型
		boolean g = true;
		boolean h = false;
	}
}